import enum
import traveller
import typing

class WeaponSize(enum.Enum):
    Small = 'Small'
    Medium = 'Medium'
    Heavy = 'Heavy'
    Vehicle = 'Vehicle'

class WeaponData(object):
    def __init__(
            self,
            name: str,
            techLevel: int,
            cost: int,
            size: WeaponSize,
            skill: traveller.SkillDefinition,
            specialty: typing.Optional[enum.Enum] = None,
            magazineCost: typing.Optional[int] = None,
            multiLink: bool = False,
            damage: str = '',
            traits: str = ''
            ) -> None:
        self._name = name
        self._techLevel = techLevel
        self._cost = cost
        self._size = size
        self._skill = skill
        self._specialty = specialty
        self._magazineCost = magazineCost
        self._multiLink = multiLink
        self._damage = damage
        self._traits = traits

    def name(self) -> str:
        return self._name
    
    def techLevel(self) -> int:
        return self._techLevel
    
    def cost(self) -> int:
        return self._cost
    
    def size(self) -> int:
        return self._size
    
    def skill(self) -> traveller.SkillDefinition:
        return self._skill
    
    def specialty(self) -> typing.Optional[enum.Enum]:
        return self._specialty

    def magazineCost(self) -> typing.Optional[int]:
        return self._magazineCost
        
    def multiLink(self) -> bool:
        return self._multiLink
    
    def damage(self) -> str:
        return self._damage
    
    def traits(self) -> str:
        return self._traits
    
